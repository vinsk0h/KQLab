// ── Moteur i18n ───────────────────────────────────────────────────────────────
(function(window) {
  'use strict';

  var STORAGE_KEY  = 'kv-lang';
  var SUPPORTED    = ['fr', 'en'];
  var DEFAULT      = 'fr';
  var _lang        = DEFAULT;
  var _listeners   = [];

  function _detectLang() {
    var stored = localStorage.getItem(STORAGE_KEY);
    if (stored && SUPPORTED.indexOf(stored) >= 0) return stored;
    var browser = (navigator.language || navigator.userLanguage || 'en').substring(0, 2).toLowerCase();
    return browser === 'fr' ? 'fr' : 'en';
  }

  function resolve(key, lang) {
    var dict = DICTS[lang] || {};
    return key.split('.').reduce(function(o, k) {
      return (o !== null && o !== undefined && o[k] !== undefined) ? o[k] : undefined;
    }, dict);
  }

  function t(key, vars) {
    var str = resolve(key, _lang);
    if (str === undefined) str = resolve(key, DEFAULT);
    if (str === undefined) str = key;
    if (typeof str !== 'string') str = key; // object path — not a leaf value
    if (vars) str = str.replace(/\{(\w+)\}/g, function(_, k) { return vars[k] !== undefined ? vars[k] : '{' + k + '}'; });
    return str;
  }

  function setLang(lang) {
    if (SUPPORTED.indexOf(lang) < 0) return;
    _lang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;
    if (typeof window.S !== 'undefined') window.S.uiLang = lang;
    applyToDOM();
    _listeners.forEach(function(fn) { try { fn(lang); } catch(e) { console.error('[i18n] listener error', e); } });
  }

  function getLang()  { return _lang; }
  function isEn()     { return _lang === 'en'; }
  function lang()     { return _lang; }

  function onLangChange(fn) { _listeners.push(fn); }

  function applyToDOM() {
    document.querySelectorAll('[data-i18n]').forEach(function(el) {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
      el.placeholder = t(el.getAttribute('data-i18n-ph'));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function(el) {
      el.title = t(el.getAttribute('data-i18n-title'));
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(function(el) {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria')));
    });
  }

  _lang = _detectLang();
  document.documentElement.lang = _lang;

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', applyToDOM);
  } else {
    applyToDOM();
  }

  window.i18n = { t: t, lang: lang, isEn: isEn, setLang: setLang, getLang: getLang, onLangChange: onLangChange, applyToDOM: applyToDOM };

})(window);
